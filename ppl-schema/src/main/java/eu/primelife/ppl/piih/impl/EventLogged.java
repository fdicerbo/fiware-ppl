package eu.primelife.ppl.piih.impl;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Inheritance;
import javax.persistence.InheritanceType;
import javax.persistence.Table;

import org.codehaus.jackson.annotate.JsonSubTypes;
import org.codehaus.jackson.annotate.JsonSubTypes.Type;
import org.codehaus.jackson.annotate.JsonTypeInfo;

@Entity
@Table(name = "EventH_")
@Inheritance(strategy = InheritanceType.JOINED)
@JsonTypeInfo(  
	    use = JsonTypeInfo.Id.NAME,  
	    include = JsonTypeInfo.As.PROPERTY,  
	    property = "type")  
	@JsonSubTypes({  
	    @Type(value = EventLoggedAccess.class, name = "access"),  
	    @Type(value = EventLoggedCreate.class, name = "create"),  
	    @Type(value = EventLoggedUpdate.class, name = "update"),  
	    @Type(value = EventLoggedDelete.class, name = "delete"),  
	    @Type(value = EventLoggedDownstream.class, name = "downstream")}) 
public abstract class EventLogged {

	protected Long id;
	
	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	
	public abstract String toString();
}
